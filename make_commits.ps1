$commitIndex = 0
foreach ($day in $days) {
    $date = $day.date
    $msgs = $day.messages
    $totalMsgs = $msgs.Count
    
    for ($i = 0; $i -lt $totalMsgs; $i++) {
        $hour = 8 + [math]::Floor($i * 10 / $totalMsgs)
        $minute = ($i * 10) % 60
        $timeStr = "$date $($hour.ToString('00')):$($minute.ToString('00')):00"
        
        $env:GIT_AUTHOR_DATE = $timeStr
        $env:GIT_COMMITTER_DATE = $timeStr
        
        # Create a small unique file change to have something to commit
        $touchFile = "$repo\.git\commit_touch.tmp"
        "$commitIndex - $($msgs[$i])" | Out-File $touchFile -Encoding utf8
        
        git add . 2>$null
        git commit --allow-empty -m $msgs[$i] 2>$null
        
        $commitIndex++
        Write-Host "[$commitIndex/500] $timeStr - $($msgs[$i])"
    }
}

